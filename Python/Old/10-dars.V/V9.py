class Human:
      def __init__(self, name, age, profession, height, weight, single):
            self.name = name
            self.age = age
            self.profession = profession
            self.height = height
            self.weight = weight
            self.single = single

      def get_name(self):
            return self.name
      def get_age(self):
            return self.age
      def get_profession(self):
            return self.profession
      def get_height(self):
            return self.height
      def get_weight(self):
            return self.weight
      def get_single(self):
            return self.single
